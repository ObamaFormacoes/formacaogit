# Palíndrome

Palind = ["arara", "casa", "rodador", "socos", "moises" , "Muoi"]
PalRes = [ 0,0,0,0,0,0 ]
Palres = [ True, False, True, True, False, False]

# Códiogo do Moises
for a in range (len(Palind)):
  RPal = (Palind[a] [::-1])
  if (RPal == Palind[a]):
    PalRes[a] = True

  else: 
    PalRes[a] = False
    
print (PalRes)

if (PalRes==Palres):
  print ("Certou Miserávi")
